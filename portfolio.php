					//$("#webSlides a").addClass("fancybox");
					//$(".slides").removeClass("slides");
				});
	}
	function ResizeScroller(width, height) {
		var hotSpotScrolling = false;
		if(width > 800)
			hotSpotScrolling = true;
		$("#webSlides").smoothDivScroll({ 
				manualContinuousScrolling: false,
				hotSpotScrolling: hotSpotScrolling,
				touchScrolling: true
			});
			$("#graphicsSlides").smoothDivScroll({ 
				manualContinuousScrolling: false,
				hotSpotScrolling: hotSpotScrolling,
				touchScrolling: true
			});
	}

