	function animateWeb() {
		$(".portNav").show();
				$(".labels").hide();
				$("#webSlides .slides").animate({
					height:100
				}, 400);
				$("#graphicsSlides").hide();
				$("#webSlides").animate({
					height: 100
				}, 500, function(){
					var elements = [];
					elements = document.getElementsByClassName('web');
					var newsize = 0;
					for(var i=0; i < elements.length; i++)
						newsize = newsize + elements[i].width;
					$("#webSlides div.scrollableArea").animate({
						width: newsize + 1
					});
					$("#graphicsDetails").slideUp("slow", function() {
						$("#webDetails").slideDown();
					});
					
					//$("#main_content").append('<a href="#" ><img class="fancybox" width="200" src="images/logo.png" /></a>');
